package com.example.demo.dto;

public class FlightCapacityDetailsDto {

	private String index;
	private String maxZeroFuelWeight;
	private String minFltw;
	private String maxTakeOffGrossWeight;
	private String maxLaunchGrossWeight;
	private String tankCap;
	private String maxRmpw;

	public String getIndex() {
		return index;
	}

	public void setIndex(String index) {
		this.index = index;
	}

	public String getMaxZeroFuelWeight() {
		return maxZeroFuelWeight;
	}

	public void setMaxZeroFuelWeight(String maxZeroFuelWeight) {
		this.maxZeroFuelWeight = maxZeroFuelWeight;
	}

	public String getMinFltw() {
		return minFltw;
	}

	public void setMinFltw(String minFltw) {
		this.minFltw = minFltw;
	}

	public String getMaxTakeOffGrossWeight() {
		return maxTakeOffGrossWeight;
	}

	public void setMaxTakeOffGrossWeight(String maxTakeOffGrossWeight) {
		this.maxTakeOffGrossWeight = maxTakeOffGrossWeight;
	}

	public String getMaxLaunchGrossWeight() {
		return maxLaunchGrossWeight;
	}

	public void setMaxLaunchGrossWeight(String maxLaunchGrossWeight) {
		this.maxLaunchGrossWeight = maxLaunchGrossWeight;
	}

	public String getTankCap() {
		return tankCap;
	}

	public void setTankCap(String tankCap) {
		this.tankCap = tankCap;
	}

	public String getMaxRmpw() {
		return maxRmpw;
	}

	public void setMaxRmpw(String maxRmpw) {
		this.maxRmpw = maxRmpw;
	}

}

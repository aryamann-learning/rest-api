package com.example.demo.dto;

public class FlightWeightParametersDto {

	private String basicOperationWeight;
	private String payLoad;
	private String zeroFuelWeight;
	private String wbZeroFuelWeight;
	private String takeOff;
	private String takeoffGrossWeight;
	private String launchGrossWeight;
	private String localMeanTimeGrossWeight;

	public String getBasicOperationWeight() {
		return basicOperationWeight;
	}

	public void setBasicOperationWeight(String basicOperationWeight) {
		this.basicOperationWeight = basicOperationWeight;
	}

	public String getPayLoad() {
		return payLoad;
	}

	public void setPayLoad(String payLoad) {
		this.payLoad = payLoad;
	}

	public String getZeroFuelWeight() {
		return zeroFuelWeight;
	}

	public void setZeroFuelWeight(String zeroFuelWeight) {
		this.zeroFuelWeight = zeroFuelWeight;
	}

	public String getWbZeroFuelWeight() {
		return wbZeroFuelWeight;
	}

	public void setWbZeroFuelWeight(String wbZeroFuelWeight) {
		this.wbZeroFuelWeight = wbZeroFuelWeight;
	}

	public String getTakeOff() {
		return takeOff;
	}

	public void setTakeOff(String takeOff) {
		this.takeOff = takeOff;
	}

	public String getTakeoffGrossWeight() {
		return takeoffGrossWeight;
	}

	public void setTakeoffGrossWeight(String takeoffGrossWeight) {
		this.takeoffGrossWeight = takeoffGrossWeight;
	}

	public String getLaunchGrossWeight() {
		return launchGrossWeight;
	}

	public void setLaunchGrossWeight(String launchGrossWeight) {
		this.launchGrossWeight = launchGrossWeight;
	}

	public String getLocalMeanTimeGrossWeight() {
		return localMeanTimeGrossWeight;
	}

	public void setLocalMeanTimeGrossWeight(String localMeanTimeGrossWeight) {
		this.localMeanTimeGrossWeight = localMeanTimeGrossWeight;
	}

}
